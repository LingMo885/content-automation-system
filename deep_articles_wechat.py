# ========== 文章生成核心函数 ==========

def generate_marriage_article():
    """生成婚姻深度文章"""
    data = random.choice(MARRIAGE_INSIGHTS)
    return format_wechat_article(data, '婚恋')


def generate_health_article():
    """生成健康深度文章"""
    data = random.choice(HEALTH_INSIGHTS)
    return format_wechat_article(data, '健康')


def generate_wealth_article():
    """生成财富深度文章"""
    data = random.choice(WEALTH_INSIGHTS)
    return format_wechat_article(data, '财富')


def format_wechat_article(data, category):
    """格式化微信公众号长文"""
    title = data['title']
    subtitle = data['subtitle']
    points = data['points']
    case = data['case_study']
    hook = data['hook']

    body = f"""{data['subtitle']}

昨天一个学员跟我说：凌墨老师，我结婚五年，越过越累，不知道是我的问题还是婚姻的问题。

我看完她的八字告诉她：不是你不够好，也不是婚姻本身有多糟糕，是你从一开始就没有看懂自己的婚姻模式。

很多人婚姻出问题，不是不会经营，是根本不知道自己属于哪种命局。不同命局的人，婚姻里的问题完全不同，需要的解决方案也完全不同。

今天用三篇文章的篇幅，把几种典型的婚姻命局说清楚。你对照着看，看看自己属于哪一种。


"""

    for i, pt in enumerate(points):
        body += f"""【{i+1}、{pt['pattern']}】

{pt['description']}

为什么会这样？

{pt['evidence']}

实际应该怎么应对？

{pt['advice']}

"""

    body += f"""【真实案例】

{case}


【给你的建议】

{hook}

---

今天的文章比较长，但我觉得值得认真读一遍。婚姻这个事情，提前知道自己是什么命局，比出了问题再找方法重要得多。

如果今天的文章对你有帮助，转发给身边正在为感情纠结的朋友。

下一期我们讲：健康篇——八字看你天生哪个器官最薄弱，以及怎么提前预防。

我是凌墨，我们下期见。
"""

    return {
        'title': title,
        'subtitle': subtitle,
        'body': body,
        'category': category
    }


def get_all_article_titles():
    """获取所有可用文章标题"""
    articles = []
    for d in MARRIAGE_INSIGHTS:
        articles.append(('婚恋', d['title']))
    for d in HEALTH_INSIGHTS:
        articles.append(('健康', d['title']))
    for d in WEALTH_INSIGHTS:
        articles.append(('财富', d['title']))
    return articles


